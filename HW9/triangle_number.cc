bool is_sum_of_triple_triangle_numbers(int number, int level) {
	if (level <= 0) {
		return !number;
	}	

	static int T[46];
	static int max;

	while (T[max] < number) {
		T[max+1] = T[max] + (max + 1);
		++max;
	}

	unsigned int pos = 0;
	while (T[pos] < number) ++pos;
	for (int i = pos; i >= 1; --i) {
		if (is_sum_of_triple_triangle_numbers(number - T[i], level - 1)) {
			return true;
		}
	}

	return false;
}

